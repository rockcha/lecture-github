/* =========================================================
🎨 바닐라 JS 미니 프로젝트
주제: 색상 팔레트 생성기 (Color Palette)

✅ 기능 체크리스트
-----------------------------------------------------------
1. 초기 색상 배열을 만들고, 초기 팔레트 화면 렌더링
2. 색상 박스 클릭 시 HEX 코드 클립보드 복사
3. [랜덤 색 생성] 버튼 클릭 시 새로운 색상 생성
4. 생성된 랜덤 색상을 각 박스에 적용

-----------------------------------------------------------

📌 컴포넌트 설계 개념
- React에서는 반복되는 UI 단위를 컴포넌트로 분리하는 것이 일반적
- 컴포넌트는 내부 로직과 상태를 숨기고,
  외부에는 필요한 API(함수)만 공개한다
- 이를 통해 재사용성, 유지보수성, 안정성, 역할 분리가 크게 향상된다
- 이러한 컴포넌트 구조는 바닐라 JavaScript에서도 충분히 구현 가능하다

-----------------------------------------------------------

📂 파일 구성
> index.js
> colorBox.js

-----------------------------------------------------------

🧠 index.js 역할 (전체 흐름 관리)
- colorBox 컴포넌트 4개 생성
- box-container에 박스 DOM 마운트
- 초기 색상 배열로 팔레트 렌더링
- [랜덤 색 생성] 버튼에 이벤트 바인딩
- 랜덤 색상 생성 후,
  colorBox가 제공하는 API(setColor)를 통해 색상 주입

-----------------------------------------------------------

🧩 colorBox.js 역할 (박스 컴포넌트)
- 색상 박스에 필요한 DOM 구조 생성
- 외부에서 사용할 수 있도록 DOM 요소(el) 반환
- setColor(color) 함수를 API로 제공
- 클릭 이벤트를 내부에서 바인딩하여
  색상 복사 기능을 컴포넌트 자체적으로 처리

========================================================= */
